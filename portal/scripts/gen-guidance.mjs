/**
 * Generator for the portal's guidance data.
 *
 * `data/guidance/*.yaml` is the single source of truth (validated by
 * `pnpm validate:guidance`). This script reads those manifests and emits
 * `portal/src/lib/guidance.data.ts` — the `GUIDANCES` array the lib's types and
 * helpers operate on. The manifests are snake_case (matching @atlas/schema);
 * the portal lib type is camelCase, so the only mapping here is
 * `last_reviewed` -> `lastReviewed` and `applies_to`/`landing_zones` ->
 * `appliesTo`/`landingZones`. Everything below the top level passes through.
 *
 * Never hand-edit guidance.data.ts: re-run this (it runs as part of
 * `pnpm build`) after changing any manifest.
 *
 * Run from anywhere: node portal/scripts/gen-guidance.mjs
 */
import { readdirSync, readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { parse } from "yaml";

const here = dirname(fileURLToPath(import.meta.url));
const GUIDANCE_DIR = join(here, "..", "..", "data", "guidance");
const OUT_FILE = join(here, "..", "src", "lib", "guidance.data.ts");

// Display/order contract: keeps listGuidance() output stable across runs
// regardless of filesystem read order. Any manifest not listed here is appended
// alphabetically (and surfaced in the log) so a new file is never silently dropped.
const ORDER = [
  "new-app-onboarding",
  "api-gateway-adoption",
  "s3-adoption",
  "textract-adoption",
];

function toAppliesTo(applies) {
  const out = {};
  if (applies.services) out.services = applies.services;
  if (applies.landing_zones) out.landingZones = applies.landing_zones;
  if (applies.guardrails) out.guardrails = applies.guardrails;
  return out;
}

function toGuidance(raw) {
  const g = {
    id: raw.id,
    title: raw.title,
    type: raw.type,
    scenario: raw.scenario,
    family: raw.family,
    objective: raw.objective,
    destination: raw.destination,
    owner: raw.owner,
    status: raw.status,
    version: raw.version,
    lastReviewed: raw.last_reviewed,
  };
  if (raw.applies_to) g.appliesTo = toAppliesTo(raw.applies_to);
  if (raw.sources) g.sources = raw.sources;
  g.steps = raw.steps; // step/task/option fields already match the lib type
  return g;
}

const byId = new Map(
  readdirSync(GUIDANCE_DIR)
    .filter((f) => f.endsWith(".yaml"))
    .map((f) => {
      const raw = parse(readFileSync(join(GUIDANCE_DIR, f), "utf8"));
      return [raw.id, toGuidance(raw)];
    }),
);

const ordered = ORDER.filter((id) => byId.has(id));
const extras = [...byId.keys()].filter((id) => !ORDER.includes(id)).sort();
if (extras.length) console.log(`gen-guidance: appended unordered manifest(s): ${extras.join(", ")}`);
const guidances = [...ordered, ...extras].map((id) => byId.get(id));

const banner = `/**
 * GENERATED by portal/scripts/gen-guidance.mjs from data/guidance/*.yaml.
 * Do not edit by hand — run \`pnpm gen:guidance\` after changing a manifest.
 */`;
writeFileSync(
  OUT_FILE,
  `${banner}\nimport type { Guidance } from "./guidance";\n\nexport const GUIDANCES: ReadonlyArray<Guidance> = ${JSON.stringify(
    guidances,
    null,
    2,
  )};\n`,
);
console.log(`gen-guidance: wrote ${guidances.length} guidance(s) to ${OUT_FILE}`);

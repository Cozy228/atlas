/**
 * One-off generator for the Regions world map.
 *
 * Emits `portal/src/components/explore/world-geo.ts`: a merged land silhouette
 * in an equirectangular (plate carrée) projection, plus the matching
 * `projectWorld(lon, lat)` helper. This lets the Regions map ship with ZERO
 * runtime map dependencies — d3-geo / topojson-client / world-atlas are used
 * here at build time only and never enter the app bundle.
 *
 * Run from the repo root:  node portal/scripts/gen-world-map.mjs
 *
 * Requires d3-geo, topojson-client and world-atlas to be resolvable from the
 * workspace pnpm store (they are dev-only tooling and intentionally NOT added
 * to any package.json). If a future checkout lacks them, install transiently:
 *   pnpm dlx --package=d3-geo --package=topojson-client --package=world-atlas \
 *     node portal/scripts/gen-world-map.mjs
 */
import { readFileSync, writeFileSync, readdirSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const ROOT = process.cwd();
const PNPM = join(ROOT, "node_modules/.pnpm");

/** Resolve a package directory inside the pnpm virtual store by name prefix. */
function findPkgDir(prefix, sub = prefix) {
  const entry = readdirSync(PNPM).find((d) => d.startsWith(`${prefix}@`));
  if (!entry) throw new Error(`Cannot find "${prefix}" under ${PNPM}. See header for install hint.`);
  return join(PNPM, entry, "node_modules", sub);
}

const d3geo = await import(join(findPkgDir("d3-geo"), "src/index.js"));
const topojson = await import(join(findPkgDir("topojson-client"), "src/index.js"));
const topology = JSON.parse(
  readFileSync(join(findPkgDir("world-atlas"), "countries-110m.json"), "utf8"),
);

// Equirectangular projection cropped to a sensible latitude band (84°N..56°S):
// it keeps every inhabited region while trimming empty polar ocean, and
// clipExtent clips the geometry to the frame so no stray edges are drawn where
// a landmass would otherwise run past the viewBox.
const WIDTH = 900;
const LAT_TOP = 84;
const LAT_BOTTOM = -56;
const SCALE = WIDTH / (2 * Math.PI);
const TRANSLATE_X = WIDTH / 2;
const TRANSLATE_Y = (SCALE * LAT_TOP * Math.PI) / 180;
const HEIGHT = Math.round(TRANSLATE_Y - (SCALE * LAT_BOTTOM * Math.PI) / 180);

const projection = d3geo
  .geoEquirectangular()
  .scale(SCALE)
  .translate([TRANSLATE_X, TRANSLATE_Y])
  .clipExtent([
    [0, 0],
    [WIDTH, HEIGHT],
  ]);
const toPath = d3geo.geoPath(projection);

// Merge every country except Antarctica (id "010") into one land silhouette.
const geometries = topology.objects.countries.geometries.filter((g) => g.id !== "010");
const land = topojson.merge(topology, geometries);

// Round to integers — at 900px wide that is ~0.4° resolution, ample for a faint
// backdrop, and roughly halves the path string. All coords are positive here.
const path = toPath(land).replace(/\d+\.\d+/g, (n) => String(Math.round(parseFloat(n))));

const banner =
  "// AUTO-GENERATED by portal/scripts/gen-world-map.mjs — do not edit by hand.\n" +
  "// Equirectangular world land silhouette for the Regions map (zero runtime map deps).\n";

const file = `${banner}
export const WORLD_MAP = {
  /** viewBox dimensions, in user units. */
  width: ${WIDTH},
  height: ${HEIGHT},
  /** Merged land silhouette, in viewBox coordinates. */
  land:
    "${path}",
} as const;

// Equirectangular projection constants — must match the generator that produced
// the land path above, so markers land exactly on their coastlines.
const SCALE = ${SCALE};
const TRANSLATE_X = ${TRANSLATE_X};
const TRANSLATE_Y = ${TRANSLATE_Y};

/** Project [lon, lat] (degrees) into WORLD_MAP viewBox coordinates. */
export function projectWorld(lon: number, lat: number): readonly [number, number] {
  const x = TRANSLATE_X + (SCALE * lon * Math.PI) / 180;
  const y = TRANSLATE_Y - (SCALE * lat * Math.PI) / 180;
  return [x, y];
}
`;

const outPath = join(
  dirname(fileURLToPath(import.meta.url)),
  "../src/components/explore/world-geo.ts",
);
writeFileSync(outPath, file);
console.log(`wrote ${outPath}`);
console.log(`viewBox ${WIDTH}x${HEIGHT}, land path ${path.length} chars`);
